function mutates(sound,mutations){
  var sounds = [];
  for (var i = 0; i < mutations; i++) {
    var newSound = sound.slice();
    newSound[5] = (i-mutations/2)*0.05 + sound[5];
    sounds.push(audio(newSound))
  }
  return sounds;
}

